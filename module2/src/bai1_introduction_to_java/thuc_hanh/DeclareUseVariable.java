package bai1_introduction_to_java.thuc_hanh;

public class DeclareUseVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s  = "Ha Noi";

        System.out.println("Variable i has value: " + i);
        System.out.println("Variable f has value: " + f);
        System.out.println("Variable d has value: " + d);
        System.out.println("Variable b has value: " + b);
        System.out.println("Variable c has value: " + c);
        System.out.println("Variable s has value: " + s);
    }
}
